import static java.lang.Math.*;

public class StaticImportDemo {
    public static void main(String[] args) {

        System.out.println("Square root of 25: " + sqrt(25));
        System.out.println("2 to the power 3: " + pow(2, 3));
        System.out.println("Max of (10, 20): " + max(10, 20));
        System.out.println("Min of (10, 20): " + min(10, 20));
        System.out.println("Absolute value of -50: " + abs(-50));
    }
}
